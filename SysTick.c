/**        \file  SysTick.c
 *        \brief  SysTick Initialization
 *
 *      \details  Set SysTick Timer to count 1 second then send an interrupt
 *
 *
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  INCLUDES
 *********************************************************************************************************************/
#include "SysTick.h"


/**********************************************************************************************************************
 *  LOCAL FUNCTIONS
 *********************************************************************************************************************/
void SysTick_1s (void)
	{
		SysTick_STRELOAD = 15999999;
		SysTick_STCTRL = 7;
		SysTick_STCURRENT = 0;
	}

