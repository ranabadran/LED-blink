/**        \file  IntCtrl.c
 *        \brief  NVIC and SCB initialization
 *
 *      \details  Assign subpriority and group priority for GPIO portF interrupt
 *								Enable Interrupt request for GPIO portF
 *
 *********************************************************************************************************************/

/**********************************************************************************************************************
 *  INCLUDES
 *********************************************************************************************************************/
#include "stdint.h"
#include "IntCtrl.h"

/**********************************************************************************************************************
 *  GLOBAL FUNCTIONS
 *********************************************************************************************************************/
void IntCtrl_Init (void)
{
	SCB_APINT_R = SCB_groupPri_8_subPri_1; 			 //3 bits for group priority and 0 bit  for sub priority
	NVIC_PRI7_R = (PRIORITY_LEVEL_6 << 5); 			 // Assign priority as 6
	NVIC_EN0_R  = (1 << IRQ_GPIOF);							 // Enable interrupt 30 in NVIC 
	}


